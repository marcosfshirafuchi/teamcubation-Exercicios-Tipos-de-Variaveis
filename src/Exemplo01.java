public class Exemplo01 {
    /**
     * <h1> Teamcubation - Tipos de Variáveis</h1>
     * Exercício 1: Tipos Primitivos<br><br>
     * Objetivo: Declarar e inicializar variáveis de tipos primitivos e imprimir seus valores.
     * <p><br>
     * <b>Note:</b> Desenvolvido na linguagem Java.
     *
     * @author  Marcos Ferreira Shirafuchi
     * @version 1.0
     * @since   21/05/2024
     */
    public static void main(String[] args) {
        byte valorByte = 1;
        short valorShort =2;
        int valorInt = 3;
        long valorLong = 70;
        float valorFloat = 99.99f;
        double valorDouble = 1999.99;
        char valorChar = 65;
        boolean valorBoolean1 = false;
        boolean valorBoolean2 = true;
        System.out.println("Valor de byte: " + valorByte);
        System.out.println("Valor de short: " + valorShort);
        System.out.println("Valor de int: " + valorInt);
        System.out.println("Valor de long: " + valorLong);
        System.out.println("Valor de float: " + valorFloat);
        System.out.println("Valor de double: " + valorDouble);
        System.out.println("Valor de char: " + valorChar);
        System.out.println("Valor de boolean: " + valorBoolean1);
        System.out.println("Valor de boolean: " + valorBoolean2);
    }
}
